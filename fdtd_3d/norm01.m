% -------------------------------------------------------------------------    
function out = norm01(in)
% -------------------------------------------------------------------------    
% Set the entries positive and its maximum value to one
%     -------------------------
%     by Michael Alvarez
%     michael.alvarez2@upr.edu
%     -------------------------
% -------------------------------------------------------------------------    
    if size(in,3) == 1 % 2d matrix
        out = abs(in) / max(max(abs(in))) ;
    else
        out = abs(in) / max(max(max(abs(in)))) ;
    end
end
